    private void fill_patient_table() {
        dtm.setRowCount(0);
        try {
            PreparedStatement stmt = con.prepareStatement("select patient_id, full_name, age, gender, phone_number, ssn, email_address, address, epc, blood_group, req_blood from patient");
            ResultSet res = stmt.executeQuery();
            while (res.next()) {
                patient_id.add(res.getInt(1));
                dtm.addRow(new Object[]{res.getString(2), res.getInt(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7), res.getString(8), res.getString(9), res.getString(10), res.getInt(11)});
            }
            table.setModel(dtm);
        } catch (SQLException ex) {
            Logger.getLogger(patientFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

