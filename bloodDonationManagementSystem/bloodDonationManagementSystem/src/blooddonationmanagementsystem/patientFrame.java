    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed

        String name = txt_full_name.getText(), 
        gender="male", 
        phone_num = txt_phone_number.getText(), 
        address = txt_pane_address.getText(), 
        epc = txt_epc.getText(),
        email_add = txt_email_add.getText(), 
        ssn = txt_ssn.getText(), 
        blood_gp = combo_blood.getSelectedItem().toString();
        int age=0, bags=1;
        if(!txt_age.getText().isEmpty()) age = Integer.parseInt(txt_age.getText());
        if(!txt_blood_bags.getText().isEmpty()) bags = Integer.parseInt(txt_blood_bags.getText());
        if (btn_male.isSelected()) gender = "male";
        else if (btn_female.isSelected()) gender = "female";
        if(txt_full_name.getText().isEmpty() || txt_ssn.getText().isEmpty() || txt_phone_number.getText().isEmpty() || txt_age.getText().isEmpty()) JOptionPane.showMessageDialog(this, "missing information");
        else{
            try {
            PreparedStatement stmt = con.prepareStatement("insert into patient (full_name, age, gender, phone_number, ssn, email_address, address, epc, blood_group, req_blood) values (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)");

            stmt.setString(1, name);
            stmt.setInt(2, age);
            stmt.setString(3, gender);
            stmt.setString(4, phone_num);
            stmt.setString(5, ssn);
            stmt.setString(6, email_add);
            stmt.setString(7, address);
            stmt.setString(8, epc);
            stmt.setString(9, blood_gp);
            stmt.setInt(10, bags);

            
            stmt.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Added successfully");
            
            fill_patient_table();
            
        } catch (SQLException ex) {
            Logger.getLogger(patientFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btn_addActionPerformed

