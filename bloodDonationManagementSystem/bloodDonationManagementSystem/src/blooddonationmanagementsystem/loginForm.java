    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        try {
            // TODO add your handling code here:
            PreparedStatement stmt = con.prepareStatement("select username , pass from login");
            ResultSet res = stmt.executeQuery();
            String username=txt_username.getText(), pass=txt_password.getText();
            boolean f=false;
            while(res.next()){
                if(!txt_username.getText().isEmpty() && !txt_password.getText().isEmpty() && res.getString(1).equals(username) && res.getString(2).equals(pass)){
                    f=true;
                    break;
                }
            }
            if(f==true) {
            this.dispose();
            new homeFrame().setVisible(true);
            }
            else JOptionPane.showMessageDialog(this, "Wrong username or password");
        } catch (SQLException ex) {
            Logger.getLogger(loginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

